// Definition for a binary tree node.
struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

// solution 1
class Solution {
public:
  bool hasPathSum(TreeNode* root, int sum) {
    if (!root) return false;
    return check(root, sum, 0);
  }

  bool check(TreeNode* node, int sum, int cur) {
    cur += node->val;
    if (!node->left && !node->right) return sum == cur;

    return (node->left && check(node->left, sum, cur)) ||
      (node->right && check(node->right, sum, cur));
  }
};


